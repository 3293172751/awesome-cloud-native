<h3 id="从成功中吸取养分" tabindex="-1"><a class="header-anchor" href="#从成功中吸取养分" aria-hidden="true">#</a> 从成功中吸取养分</h3>
<p>中国人常常说：失败是成功子母，创业倒闭者十有八九，还有人说失败能铸造品行，甚至还有人说，早死早超生，所以早点失败。</p>
<p>这些观点很愚昧。人要从失败中吸取教训，那么到底从失败中吸取到的什么？我们徒步的时候第一次喜欢带很多东西，最后发现走不动了。于是我们吸取到教训，第二次少带一些。但是这样的意义很大吗。我还是不知道第二次该带什么，该怎么样才能顺利走完。</p>
<p>相比之下，我们更应该从成功中吸取养分，成功才是真正靠得住的教材。做成一件事情后，你就会知道这条路行得通，然后你下次就会再次这么做，而下一次，你也许会做的更棒。</p>
<p>哈佛学校研究表明：<strong>那些已经成功的企业家们再创辉煌的可能性远比常人大得多。所以成功是一个极具现实价值的宝贵经验。</strong></p>

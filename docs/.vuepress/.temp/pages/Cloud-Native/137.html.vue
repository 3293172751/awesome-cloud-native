<h3 id="工作狂" tabindex="-1"><a class="header-anchor" href="#工作狂" aria-hidden="true">#</a> 工作狂</h3>
<p>工作狂的行为在很多时候是没有必要的，过多的工作不代表你对项目更专注，也不代表你做了更多的贡献，而是仅仅意味着你做了更多的活。</p>
<p>正确的模式，并不是无脑的加班，如果只是为了工作而工作，那么你就会丧失判断力，你的价值观和决策方式都是扭曲的。你没有办法去判断哪些工作是值得做，哪些工作是该放弃的，最后自己筋疲力尽，而一个筋疲力尽的人是无法做出明智的决定。</p>
<p>正确的方式，是早早的找到，并且解决问题，下班休息哈哈哈，工作是做不完的，所以不要通过加班的形式让自己解脱，留点时间去思考对策或者优化方案，更有利于更快更好的工作。</p>

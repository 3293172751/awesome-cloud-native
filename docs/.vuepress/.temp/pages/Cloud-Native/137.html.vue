<p>一个小的公司能有多小？小公司往往在人们的眼中都是不屑一顾的，当你的公司很大，你身边的亲戚就会很自然的认为你的公司一定是很强大的，很优秀的，门槛很高的。而如果你的公司仅仅是几人或者是十几人的小公司，那么他们肯定会认为是小孩把戏。</p>

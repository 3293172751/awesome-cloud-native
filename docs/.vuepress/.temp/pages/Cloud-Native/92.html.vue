<h5 id="overview" tabindex="-1"><a class="header-anchor" href="#overview" aria-hidden="true">#</a> Overview</h5>
<p>The <a href="https://github.com/openim-sigs/component-base" target="_blank" rel="noopener noreferrer">openim-sigs/component-base<ExternalLinkIcon/></a> repository stands distinct from the <a href="https://github.com/OpenIMSDK/tools" target="_blank" rel="noopener noreferrer">OpenIMSDK/tools<ExternalLinkIcon/></a> repository. Each serves a unique purpose in the ecosystem and caters to different consumers.</p>
<h4 id="primary-consumers-of-openimsdk-tools" tabindex="-1"><a class="header-anchor" href="#primary-consumers-of-openimsdk-tools" aria-hidden="true">#</a> Primary Consumers of OpenIMSDK/tools</h4>
<p>The main consumers for the <a href="https://github.com/OpenIMSDK/tools" target="_blank" rel="noopener noreferrer">OpenIMSDK/tools<ExternalLinkIcon/></a> repository are:</p>
<ul>
<li><a href="https://github.com/OpenIMSDK/Open-IM-Server/" target="_blank" rel="noopener noreferrer">OpenIMSDK/Open-IM-Server/<ExternalLinkIcon/></a></li>
<li><a href="https://github.com/OpenIMSDK/chat" target="_blank" rel="noopener noreferrer">OpenIMSDK/chat<ExternalLinkIcon/></a></li>
<li><a href="https://github.com/OpenIMSDK/Openim-sdk-core" target="_blank" rel="noopener noreferrer">OpenIMSDK/Openim-sdk-core<ExternalLinkIcon/></a></li>
</ul>
<p>These entities leverage the utilities and tools offered by this repository for their functioning and enhancements.</p>
<h5 id="purpose-of-openim-sigs-component-base" tabindex="-1"><a class="header-anchor" href="#purpose-of-openim-sigs-component-base" aria-hidden="true">#</a> Purpose of openim-sigs/component-base</h5>
<p>In contrast, the <a href="https://github.com/openim-sigs/component-base" target="_blank" rel="noopener noreferrer">openim-sigs/component-base<ExternalLinkIcon/></a> repository houses core foundational components. These are designed to be used externally, offering a broad spectrum of utilities and functions. Its scope of influence and application, thus, tends to be more expansive than that of the OpenIMSDK/tools.</p>

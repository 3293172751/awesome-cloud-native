<h2 id="介绍" tabindex="-1"><a class="header-anchor" href="#介绍" aria-hidden="true">#</a> 介绍</h2>
<p>很早之前就了解到了 Auto-GPT，作为 GitHub 上近期增长速度最快的项目（没有之一），Auto-GPT 在开源社区可谓是人尽皆知，甚至 star 已经很快就超过 Kubernetes，目前有 123k star。</p>
<p>得益于 Auto-GPT 的出色技术，可以高精度和高效率地自动执行许多任务。 它利用了 GPT-4 强大的自然语言处理功能。</p>
<p>我们甚至可以通过它来实现更多的自动化的工作，比如说前一节 <RouterLink to="/Cloud-Native/49.html">在 Sealos 上开发一款 AI 自动云原生化项目自动上线工具</RouterLink></p>
<h2 id="什么是-autogpt" tabindex="-1"><a class="header-anchor" href="#什么是-autogpt" aria-hidden="true">#</a> 什么是 AutoGPT</h2>
<p>它的 GitHub 地址：</p>
<ul>
<li><a href="https://github.com/Significant-Gravitas/Auto-GPT" target="_blank" rel="noopener noreferrer">GitHub<ExternalLinkIcon/></a></li>
</ul>
<p>从本质上讲，Auto-GPT 利用 OpenAI 最新人工智能模型的多功能性与软件和服务进行在线互动，使其能够 “自主 “执行X和Y等任务。但正如我们在大型语言模型方面的学习，这种能力似乎像海洋一样宽广，但却像水坑一样深。</p>
<p>AutoGPT 是一个由人工智能驱动的应用程序，利用 GPT-4 等 LLM 的强大功能自主创建和处理各种工作。通过使用 Auto GPT，组织和个人可以简化报告创作、内容创建和数据分析等流程，以节省时间并减少错误。</p>
<p>AutoGPT 改变了任务自动化的游戏规则，使组织和个人能够专注于其他关键任务，同时将重复和琐碎的工作留给程序。</p>
<p>随着 LLM 的不断发展，我们可以期待看到像 Auto GPT 这样功能越来越强大的软件能够执行越来越复杂的任务。</p>
<p>就人工智能驱动的技术将如何改变我们未来的操作方式和与人工智能系统的互动方式而言，AutoGPT 提供了一个新方向。</p>
<h2 id="它是如何工作的" tabindex="-1"><a class="header-anchor" href="#它是如何工作的" aria-hidden="true">#</a> 它是如何工作的</h2>
<p>Auto GPT 使用 LLM 的最新发展，特别是 GPT-4，自动生成具有凝聚力和相关性的内容。该程序从大量的数据中学习，这使它能够识别单词和句子之间的模式和联系。</p>
<p>使用这些信息，Auto GPT 会生成文本以响应提示或输入。这种输入可能以指令、任务或一套指导方针的形式出现。</p>
<p>Auto GPT 在收到输入后，使用其尖端的算法和自然语言处理技能，创建上下文适当且逻辑一致的内容。对于希望自动化流程和节省时间的组织和人员来说，Auto GPT 是一种重要的资源，因为它生成的文本几乎与人类书面语言无法区分。</p>
<p>Auto GPT 的优势在于它能够从大量数据中学习，并生成相关和合乎逻辑的文本，使其成为作业自动化领域的关键工具。</p>
<p>与免费版的ChatGPT不同，<strong>Auto-GPT可以连接到互联网</strong>，找到任何主题的最新信息。因此，你可以用它来访问任何网页并捕获信息。</p>
<h3 id="可以做哪些事情" tabindex="-1"><a class="header-anchor" href="#可以做哪些事情" aria-hidden="true">#</a> 可以做哪些事情</h3>
<p>Auto GPT是一个灵活的程序，可用于各种活动，包括创建报告和数据分析。在这一部分中，我们将了解 Auto GPT可以执行的一些功能，以及它是如何自动执行这些功能的。</p>
<p><strong>内容创建</strong></p>
<p>网站、博客和社交媒体帖子的内容可以使用 Auto GPT 创建。如果你给它一个主题或一套指导方针，Auto GPT 可以产生高质量、相关性和趣味性的材料。</p>
<p><strong>翻译</strong></p>
<p>你可以使用 Auto GPT 执行翻译任务。通过使用 Auto GPT 以一种语言作为输入文本，可以将文本翻译成另一种语言。在不同国家/地区开展业务并需要快速文档或通信翻译的企业可以提供非常大帮助。</p>
<p><strong>客户服务</strong></p>
<p>客户支持职责，如响应频繁的查询和解决问题，可以通过 Auto GPT 实现自动化。Auto GPT 可以使用自然语言处理来理解客户查询并提供相关的解决方案。</p>
<p><strong>数据分析</strong></p>
<p>可以使用 Auto GPT 执行数据分析活动。数据输入允许 Auto GPT 分析信息并产生可用于决策的见解。</p>
<p><strong>撰写报告</strong></p>
<p>企业和研究人员可以使用 Auto GPT 根据数据输入生成报告。通过输入数据，Auto GPT可以分析信息并产生准确和有指导意义的结果。</p>
<p><strong>编码</strong></p>
<p>Auto GPT 可用于编码作业生成完整的程序或代码片段。Auto GPT 可以通过考虑编程参数或需求来生成有效且高效的代码。需要精确快速地编写代码的开发人员会发现这种功能非常有帮助。</p>
<p><em>发挥你的想象力，只有你想不到的，没有它做不到的~</em></p>
<h2 id="搭建和设置环境" tabindex="-1"><a class="header-anchor" href="#搭建和设置环境" aria-hidden="true">#</a> 搭建和设置环境</h2>
<p>如果你可以**<a href="https://www.wbolt.com/how-to-use-gpt-4-free.html" target="_blank" rel="noopener noreferrer">使用GPT-4<ExternalLinkIcon/></a> API**，Auto-GPT的效果最好，因为它更善于思考并得出结论。它也不太容易产生幻觉。如果你还没有访问权限，你可以<a href="https://www.wbolt.com/go?_=18a71268abaHR0cHM6Ly9vcGVuYWkuY29tL3dhaXRsaXN0L2dwdC00LWFwaQ%3D%3D" target="_blank" rel="noopener noreferrer">使用这里的链接<ExternalLinkIcon/></a>加入GPT-4 API访问的等待名单。然而，你也可以使用普通的OpenAI API与GPT-3.5模型。</p>
<p><strong>准备工作：</strong></p>
<ol>
<li>
<p>Git</p>
</li>
<li>
<p>Python 3.8 or later</p>
</li>
<li>
<p>OpenAI API key</p>
</li>
<li>
<p>PineCone API key</p>
</li>
</ol>
<p>用 git clone:</p>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code>❯ <span class="token function">git</span> clone https://github.com/Significant-Gravitas/Auto-GPT
❯ <span class="token builtin class-name">cd</span> Auto-GPT
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div></div></div><p>安装：</p>
<blockquote>
<p>requirements.txt 文件是一个文本文件，通常用于 Python 项目中，其中包含了该项目所需的所有依赖包及其版本信息。</p>
<p>我们之前在学习 buildpacks 项目的时候，它如何解决 python 的环境判断问题，就是通过 requirements.txt 文件。</p>
</blockquote>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code>❯ pip <span class="token function">install</span> <span class="token parameter variable">-r</span> requirements.txt
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p>之后，将<code v-pre>.env.template</code> 重命名为 <code v-pre>.env</code>，并使用 OpenAI 和 PineCone API 密钥填充字段。</p>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code>❯ <span class="token function">mv</span> .env.template .env
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p>之后，转到 VIM ，在 <code v-pre>OPENAI_API_KEY</code> 部分粘贴API。你可以参考下面的图片来了解一下。</p>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code>❯ <span class="token function">cat</span> .env <span class="token operator">|</span> <span class="token function">grep</span> <span class="token parameter variable">-i</span> OPENAI_API_KEY
<span class="token comment">## OPENAI_API_KEY - OpenAI API Key (Example: my-openai-api-key)</span>
<span class="token assign-left variable">OPENAI_API_KEY</span><span class="token operator">=</span>your-openai-api-key
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div><div class="line-number"></div><div class="line-number"></div></div></div><p>接下来，打开<a href="https://www.wbolt.com/go?_=baa37b74edaHR0cHM6Ly93d3cucGluZWNvbmUuaW8v" target="_blank" rel="noopener noreferrer">pinecone.io<ExternalLinkIcon/></a>并创建一个免费账户。它将允许LLM从内存中检索相关信息，用于AI应用。</p>
<p>在这里，点击左侧边栏的 <code v-pre>API Keys</code>，并点击右侧窗格的 <code v-pre>Create API Key</code></p>
<p>给一个名字，如 <code v-pre>autogpt</code>，然后点击 <code v-pre>Create Key</code></p>
<p><img src="http://sm.nsddd.top/sm202305031712718.png" alt="image-20230503171039991"></p>
<p>复制 Key Value，用 <code v-pre>vim</code> 打开，将其粘贴在 <code v-pre>PINECONE_API_KEY</code> 旁边。</p>
<p>同样地，复制 <code v-pre>Environment</code> 下的数值。</p>
<p>现在，把它粘贴到<code v-pre>PINECONE_ENV</code>旁边。</p>
<h2 id="运行" tabindex="-1"><a class="header-anchor" href="#运行" aria-hidden="true">#</a> 运行</h2>
<p>打开一个终端来执行 <code v-pre>main.py</code> Python 脚本。</p>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code>❯ python3 <span class="token parameter variable">-m</span> autogpt
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p><strong>为 AI 命名：</strong></p>
<p>在第一次运行时，Auto-GPT会要求你<strong>为AI命名</strong>。如果你不想为一个特定的用例创建一个人工智能，你可以把这个字段留空并点击回车。它默认加载的是Entrepreneur-GPT名称。</p>
<p><strong>定义人工智能的作用~</strong></p>
<p>之后，<strong>为自主AI逐一设定目标</strong>。这是你告诉人工智能你想实现的目标的地方。你可以要求它将信息保存在一个文本或PDF文件中。你也可以要求它在检索完所有信息后关闭。</p>
<p><strong>定义 AI 角色：</strong></p>
<p>根据你希望 AI 发挥的功能，为其命名和角色，例如“研究人员”、“内容生成器”或“个人编码器”。为了获得更成功的结果，明确你希望人工智能实现的目标。</p>
<p><strong>设定目标：</strong></p>
<p>详细概述人工智能的目标，例如获取信息、将数据存储在文件中、执行代码或修改文本。包括要使用的输出文件的信息，以及完成作业所需的任何其他操作。</p>
<p>目标如下：</p>
<ol>
<li>
<p>开发产品</p>
</li>
<li>
<p>优化产品</p>
</li>
<li>
<p>扩大产品规模</p>
</li>
<li>
<p>创造5000万美元以上的收入</p>
</li>
<li>
<p>保持这种一致性</p>
</li>
</ol>
<p>Auto-GPT将开始思考。在行动过程中，它将要求你授权行动。<strong>按 “y”</strong>，然后按<strong>回车键</strong>确认。它可能会连接到网站并收集信息。</p>
<p>你可以读懂<strong>人工智能在思考、推理和计划什么</strong>。它还提供批评（一种负面的提示），以便它拿出正确的信息。最后，它执行行动。</p>

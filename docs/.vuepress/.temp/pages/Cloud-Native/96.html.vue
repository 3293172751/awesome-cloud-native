<h1 id="openim-远程工作团队协作协议-v1-3" tabindex="-1"><a class="header-anchor" href="#openim-远程工作团队协作协议-v1-3" aria-hidden="true">#</a> OpenIM 远程工作团队协作协议 v1.3</h1>
<h2 id="principles" tabindex="-1"><a class="header-anchor" href="#principles" aria-hidden="true">#</a> Principles</h2>
<h3 id="_0-ownership-leadership" tabindex="-1"><a class="header-anchor" href="#_0-ownership-leadership" aria-hidden="true">#</a> 0）Ownership &amp; Leadership</h3>
<p>如果看到团队或是项目有问题的时候，不要等，也不忍，请马上说出来，并给出相应的方案， <strong>自己跳出来召集开会，及时调整。不要闷在那里，自己憋！</strong></p>
<p>“每个团队成员都承担Owner和Leader的角色。发现问题时，勇于指出并提供解决方案，不要等待或沉默。”</p>
<h3 id="_1-initiative" tabindex="-1"><a class="header-anchor" href="#_1-initiative" aria-hidden="true">#</a> 1）Initiative</h3>
<p>每人个都必需是主动的，都需要自己发起要做的事，或是自己要认领要做的事，如果发现自己没有事情了， <strong>需要学会主动发现问题，主动找到可以improve的地方，创新来源于此</strong>。没有路要学会自己造路！</p>
<p>“为团队创新和改进提供动力。”</p>
<h3 id="_2-objectives-oriented" tabindex="-1"><a class="header-anchor" href="#_2-objectives-oriented" aria-hidden="true">#</a> 2）Objectives Oriented</h3>
<p>每个人都是产品经理，也都是项目经理，每个人都必需把自己的工作和我们大的目标连接在一起，知道什么是重点，重点的东西就是两件事：一）从用户的角度出发，二）从产品的角度出发。 <strong>这意味着我们要随时观察整个产品的样子，而不只是自己这一块东西</strong> 。</p>
<p>“始终保持用户和产品的双重视角，确保工作与总体目标一致。”</p>
<h3 id="_3-insists-on-high-standard" tabindex="-1"><a class="header-anchor" href="#_3-insists-on-high-standard" aria-hidden="true">#</a> 3）Insists on High Standard</h3>
<p>举法其上，得乎其中，举法其中，得乎其下，举法其下，法不得也。我们要坚持用高的标准要求自己，对于高标准的目标不妥协，但是在实施路径和策略上可以妥协。</p>
<p>“始终坚持高标准，确保质量。在实施过程中可以灵活，但对于高标准的最终目标绝不妥协。”</p>
<h2 id="practices" tabindex="-1"><a class="header-anchor" href="#practices" aria-hidden="true">#</a> Practices</h2>
<h3 id="_0-online" tabindex="-1"><a class="header-anchor" href="#_0-online" aria-hidden="true">#</a> 0）Online</h3>
<p>工作的时候必需在线。如果不在线了，需要说一下不在线的时长, 目前我们工作的事宜在通讯工具采用Slack， 如果需要请假的情况，如果不是紧急情况，需要<strong>提前一天</strong> 在MegaEase的Slack <em>#random</em> 频道中提前说明。如果是紧急情况，也需要提前在<em>insider</em>频道中告知大家。</p>
<p>“工作时，请确保在线。若需离线，务必在Slack <em>#insider</em> 频道提前通知。”</p>
<h3 id="_1-documentation-driven" tabindex="-1"><a class="header-anchor" href="#_1-documentation-driven" aria-hidden="true">#</a> 1) Documentation Driven</h3>
<p>面对面交谈、电话语音、微信、Slack虽然是比较实时的反馈工具，但是只有文档是可以把重要信息给结构化的，而且写文档其实是比起前面的方式来说是更为深度的思考，因为会让你自己审视自己的想法。所以，对于一些重要 “<strong>功能</strong>”、“<strong>流程</strong>”、“<strong>业务逻辑</strong>” 、“<strong>设计</strong>”、“<strong>问题</strong>”，以及“<strong>想法</strong>”，最好都以文档化的方式进行。请使用Github的 wiki、project、issue这些工具或是使用Google Doc.</p>
<p>“确保关键信息持久化和可追溯。”</p>
<h3 id="_2-design-review" tabindex="-1"><a class="header-anchor" href="#_2-design-review" aria-hidden="true">#</a> 2）Design Review</h3>
<p>对于一些重要的问题或是工作（每个人都能够判断什么是关键问题和工作）， <strong>需要先把自己的想法share出来，而不是先实现</strong> 。</p>
<p>一个好的 Design 文档需要包括如下项：</p>
<ul>
<li>
<p><strong>Background</strong>。交待这个事的背景、需求和要解决问题。</p>
</li>
<li>
<p><strong>Objectives</strong>。说明这个事的目标和意义。</p>
</li>
<li>
<p>Alternative Solutions</p>
<p>。 给出多个解决方案，并能够进行 Pros/Cons 对比。</p>
<ul>
<li><strong>Reference</strong>。方案需要有权威引用支持。</li>
<li><strong>Data</strong>。方案需要有相关数据数据支持。</li>
</ul>
</li>
<li>
<p><strong>Conclusion</strong>。结论是什么。</p>
</li>
</ul>
<p>“在开始实施前，确保与团队分享并得到认同。”</p>
<h3 id="_3-simplification-automation" tabindex="-1"><a class="header-anchor" href="#_3-simplification-automation" aria-hidden="true">#</a> 3) Simplification &amp; Automation</h3>
<p>简化和自动化是软件工程所追求的两大目标，简化不是简陋，简化是对事物一种抽象和归纳能力，其能够提升软件的复用能力和扩展性，自动化是工程能力的重要体现，一方面，远程工作中自动化的能力可以让整个团队更高效地协作，另一方面，自动化是规模化的提条件。所以，我们要无时无刻地思考如何简化和自动化现有的事情。</p>
<h3 id="_4-review-re-factory" tabindex="-1"><a class="header-anchor" href="#_4-review-re-factory" aria-hidden="true">#</a> 4）Review &amp; Re-factory</h3>
<p>无论是代码还是工作都是需要反思和重构的。反思是进步的源泉，项目告一段落时，出现问题时，都应该召集团队做集体反思，把好的东西坚持下去，把不好的东西优化掉。这样才能进步和改进。但是任何的优化措施是可执行的。</p>
<h3 id="_5-milestone-commitment" tabindex="-1"><a class="header-anchor" href="#_5-milestone-commitment" aria-hidden="true">#</a> 5）Milestone Commitment</h3>
<p>对于一个项目，每个人都需要有自己的 milestone 计划， <strong>这个计划最好是在2周以内，1周内是最好的。而且要承诺到</strong> 。</p>
<p>“确保为每个项目设定并遵循里程碑计划。”</p>
<h3 id="_6-evidence-driven" tabindex="-1"><a class="header-anchor" href="#_6-evidence-driven" aria-hidden="true">#</a> 6）Evidence Driven</h3>
<p>任何讨论和分析都要基于权威的证据、数据或是引用。在我们做设计的时候，或是有争论的时候，说服对方最好的方式就是拿出证据、数据或是权威引用。比如：我的XX设计参考了TCP协议中的XX设计，我的XX观点是基于XX开源软件的实现……如果争论不休就停止争论，然后各自收集和调查自己观点的佐证。</p>
<h3 id="_7-demo-day" tabindex="-1"><a class="header-anchor" href="#_7-demo-day" aria-hidden="true">#</a> 7）Demo Day</h3>
<p>把自己做的东西跟团队做一次实时的演示。这样有助于开发人员从产品角度思考自己的工作。除了演示产品功能，还可以演示算法，设计，甚至代码。</p>
<p>“这也鼓励团队成员互相学习和分享。”</p>
<h3 id="_8-effective-meeting" tabindex="-1"><a class="header-anchor" href="#_8-effective-meeting" aria-hidden="true">#</a> 8) Effective Meeting</h3>
<p>会议主要处理三件事：提出议案、发现问题、共识结论。</p>
<ul>
<li>会议不仅仅要有议题，最好还有议案。</li>
<li>会议期间不解决问题，只发现问题，和跟踪问题。</li>
<li>会议必需要有共识和结论，如果不能达到共识和结论，那就当成问题处理，由问题的负责人跟进问题。</li>
</ul>
<p>关于周会或是临时性的团队会议（私下讨论不属于会议），会议组织者需要在事前收集会议议题，其中包括如下分类：</p>
<ul>
<li><strong>项目类</strong>：需要事先有项目进度计划表（任何分项最好控制在1-2人周内）</li>
<li><strong>方案类</strong>：需要事先写好相关的方案和设计才能讨论（参看 Design Review 章节）</li>
<li><strong>问题类</strong>：需要事先写好相关的问题和解决提案（参看 Design Review 章节）</li>
<li><strong>决策类</strong>：需要事先写好整事的前因后果以及利弊分析</li>
<li><strong>信息类</strong>：需要事先写好相关的事宜说明</li>
</ul>
<p>组织者需要在周五的时候发出会议议题收集，其中包括：</p>
<ul>
<li>自己知道的项目的进度跟进（需要相相关的项目负责人准备相关的项目计划）</li>
<li>方案和问题类的需要各个项目负责人提出来，并有相关的设计文档可供Review</li>
<li>信息类和决策类的事宜可以写在Google Doc上，也可以写在 Team 的 Issue 里</li>
</ul>
<p>其它负责人可以在会议上加入自己团队的东西，或是要求其他团队提供更多的信息。</p>
<p>“务必确保每次会议都是高效和有成果的。”</p>
<h3 id="_9-1-2-3-escalation" tabindex="-1"><a class="header-anchor" href="#_9-1-2-3-escalation" aria-hidden="true">#</a> 9）1-2-3 Escalation</h3>
<p>遇到问题的时候，自己一个人处理1小时内没有思路，请找他人小范围讨论，如果与他人2小时内没有结果，请上升到团队范围，如果在团队范围3小时内没有思路，我们就需要借助外部力量了。</p>
<p>“当遇到问题，如果1小时内不能自己解决，请与同事讨论。2小时内与同事未能解决，提升至团队讨论。若3小时内团队未能解决，考虑寻求外部帮助。”</p>
<h3 id="a-3ps-update" tabindex="-1"><a class="header-anchor" href="#a-3ps-update" aria-hidden="true">#</a> A）3PS Update</h3>
<p>每个人更新进度的时候，不要只是一个check-in，而是需要更 meaningful 的说一下工作内容，在工作告一段落的时候，希望简单的说一下工作总结。这里的practice是： <strong>3PS – Plan，Proirity，Problem，Summary， – 你的计划是什么？优先级是什么？遇到了什么问题？当前的工作摘要</strong> 。</p>
<p>“3PS进度更新”和调整描述：“在更新工作进度时，分享你的计划(Plan)，优先级(Priority)，遇到的问题(Problem)，以及工作摘要(Summary)。”</p>
<h3 id="b-disagree-and-commitment" tabindex="-1"><a class="header-anchor" href="#b-disagree-and-commitment" aria-hidden="true">#</a> B) Disagree and Commitment</h3>
<p>在我们开发的时候，团队的成员都会有自己风格，必然会对同一个问题产生较大的争议（Disagree），我们鼓励有争议，但是是在团队的决议作出之前。一旦团队形成决议，团队的成员就必须支持这个决议，并在这个方向上做出贡献。</p>
<p>但是关于决议的形成过程肯定充斥着各种的争论，对于这些争论，我们可以按照下面的Guidline 来处理争议：</p>
<ul>
<li>Owner要负责对重大的讨论推进，尽快形成结论。</li>
<li>在决议过程中，要有纪要，要更新到 Github 相关项目的 Issue 或 Pull Request 里，并且要让整个团队知道，信息平等很重要。</li>
<li>不要妥协，坚持高的标准。第一标准是工业标准，第二标准是国外的大公司标准（如：google, fb, github, aws…），第三标准才是国内的标准。</li>
<li>那怕再复杂，只要是标准，就可以说服用户。用户再无理，也不可能反对工业级的标准。</li>
<li>Release出去的东西，只要被用户用上了，要改就难了，所以要谨慎而果敢。</li>
</ul>
<p>“在团队决定之后，即使有不同意见，也需要全力支持团队的决策。”</p>
<h1 id="openim-remote-team-collaboration-protocol-v1-3" tabindex="-1"><a class="header-anchor" href="#openim-remote-team-collaboration-protocol-v1-3" aria-hidden="true">#</a> OpenIM Remote Team Collaboration Protocol v1.3</h1>
<h2 id="principles-1" tabindex="-1"><a class="header-anchor" href="#principles-1" aria-hidden="true">#</a> Principles</h2>
<h3 id="_0-ownership-leadership-1" tabindex="-1"><a class="header-anchor" href="#_0-ownership-leadership-1" aria-hidden="true">#</a> 0) Ownership &amp; Leadership</h3>
<p>When you see issues with the team or project, don't wait or endure. Speak up immediately, propose solutions, <strong>initiate a meeting yourself, and adjust promptly. Don't suppress your concerns!</strong></p>
<p>&quot;Every team member embodies the roles of both Owner and Leader. When issues arise, be proactive in pointing them out and offering solutions. Don’t just wait or stay silent.&quot;</p>
<h3 id="_1-initiative-1" tabindex="-1"><a class="header-anchor" href="#_1-initiative-1" aria-hidden="true">#</a> 1) Initiative</h3>
<p>Everyone must take the initiative. Whether it’s about starting something or claiming a task, if you find yourself idle, <strong>proactively identify problems and areas of improvement. Innovation stems from this</strong>. If there’s no path, pave one yourself!</p>
<p>&quot;Drive innovation and improvements for the team.&quot;</p>
<h3 id="_2-objectives-oriented-1" tabindex="-1"><a class="header-anchor" href="#_2-objectives-oriented-1" aria-hidden="true">#</a> 2) Objectives Oriented</h3>
<p>Everyone is a product manager and project manager. Everyone needs to link their work to our broader objectives, understanding what's crucial. Two main things matter: 1) From the user's perspective, 2) From the product's perspective. <strong>This means we must always observe the entire product, not just our specific segment</strong>.</p>
<p>&quot;Always maintain a dual perspective of users and products, ensuring that work aligns with the overarching goals.&quot;</p>
<h3 id="_3-insists-on-high-standards" tabindex="-1"><a class="header-anchor" href="#_3-insists-on-high-standards" aria-hidden="true">#</a> 3) Insists on High Standards</h3>
<p>Aim high, achieve the middle; aim for the middle, fall short; aim low, miss altogether. We must persistently hold ourselves to high standards, not compromising on these high objectives, but being flexible in our approaches and strategies.</p>
<p>&quot;Always maintain high standards, ensuring quality. Be flexible during the execution, but never compromise on the ultimate high standards.&quot;</p>
<h2 id="practices-1" tabindex="-1"><a class="header-anchor" href="#practices-1" aria-hidden="true">#</a> Practices</h2>
<h3 id="_0-online-1" tabindex="-1"><a class="header-anchor" href="#_0-online-1" aria-hidden="true">#</a> 0) Online</h3>
<p>You must be online when working. If you go offline, notify others about the duration. For communication, we use Slack. If you need time off, unless it's urgent, <strong>inform a day in advance</strong> on MegaEase's Slack <em>#random</em> channel. In urgent situations, notify the <em>insider</em> channel.</p>
<p>&quot;When working, ensure you are online. If you need to go offline, notify in advance on Slack's <em>#insider</em> channel.&quot;</p>
<h3 id="_1-documentation-driven-1" tabindex="-1"><a class="header-anchor" href="#_1-documentation-driven-1" aria-hidden="true">#</a> 1) Documentation Driven</h3>
<p>Face-to-face conversations, calls, WeChat, and Slack provide real-time feedback, but only documentation offers structured vital information. Writing documents also facilitates deeper thinking as it allows you to scrutinize your ideas. Hence, crucial <strong>&quot;functions&quot;</strong>, <strong>&quot;processes&quot;</strong>, <strong>&quot;business logic&quot;</strong>, <strong>&quot;designs&quot;</strong>, <strong>&quot;issues&quot;</strong>, and <strong>&quot;ideas&quot;</strong> should ideally be documented. Use tools like Github's wiki, project, issue, or Google Doc.</p>
<p>&quot;Ensure key information is persistent and traceable.&quot;</p>
<h3 id="_2-design-review-1" tabindex="-1"><a class="header-anchor" href="#_2-design-review-1" aria-hidden="true">#</a> 2) Design Review</h3>
<p>For critical tasks or issues (everyone can discern what’s vital), <strong>share your ideas first before implementing</strong>.</p>
<p>A good design document should include:</p>
<ul>
<li><strong>Background</strong>: Context, requirements, and problem description.</li>
<li><strong>Objectives</strong>: Aim and significance of the task.</li>
<li><strong>Alternative Solutions</strong>: Provide various solutions and compare their pros and cons.
<ul>
<li><strong>Reference</strong>: Solutions must have authoritative references.</li>
<li><strong>Data</strong>: Solutions must be backed by relevant data.</li>
</ul>
</li>
<li><strong>Conclusion</strong>: Final takeaways.</li>
</ul>
<p>&quot;Before initiating, ensure that you share your ideas with the team and gain consensus.&quot;</p>
<h3 id="_3-simplification-automation-1" tabindex="-1"><a class="header-anchor" href="#_3-simplification-automation-1" aria-hidden="true">#</a> 3) Simplification &amp; Automation</h3>
<p>Simplification and automation are two main goals in software engineering. Simplification isn't about being simplistic but about abstraction and induction, enhancing software reusability and scalability. Automation showcases engineering prowess. In remote work, automation boosts team efficiency, and on the other hand, it's a prerequisite for scaling. Therefore, we must always think about how to simplify and automate existing tasks.</p>
<h3 id="_4-review-re-factory-1" tabindex="-1"><a class="header-anchor" href="#_4-review-re-factory-1" aria-hidden="true">#</a> 4) Review &amp; Re-factory</h3>
<p>Everything, be it code or work processes, requires introspection and refactoring. Introspection is the key to improvement. At the end of a project or when issues arise, call a team meeting for collective reflection. Keep the good practices and optimize the rest. But any optimization must be actionable.</p>
<h3 id="_5-milestone-commitment-1" tabindex="-1"><a class="header-anchor" href="#_5-milestone-commitment-1" aria-hidden="true">#</a> 5) Milestone Commitment</h3>
<p>For every project, everyone should have their own milestone plans. <strong>These plans ideally should be within a week, but definitely within two weeks. And they must be committed to</strong>.</p>
<p>&quot;Ensure milestones are set and adhered to for every project.&quot;</p>
<h3 id="_6-evidence-driven-1" tabindex="-1"><a class="header-anchor" href="#_6-evidence-driven-1" aria-hidden="true">#</a> 6) Evidence Driven</h3>
<p>All discussions and analyses should be based on authoritative evidence, data, or references. When designing or in disagreements, the best way to persuade others is by presenting evidence, data, or authoritative references. If debates go on endlessly, stop and collect supporting evidence for your arguments.</p>
<h3 id="_7-demo-day-1" tabindex="-1"><a class="header-anchor" href="#_7-demo-day-1" aria-hidden="true">#</a> 7) Demo Day</h3>
<p>Show and tell with the team what you've created. This helps developers think about their work from a product perspective. Besides product features, you can also showcase algorithms, designs, and even codes.</p>
<p>&quot;This encourages team members to learn from and share with each other.&quot;</p>
<h3 id="_8-effective-meeting-1" tabindex="-1"><a class="header-anchor" href="#_8-effective-meeting-1" aria-hidden="true">#</a> 8) Effective Meeting</h3>
<p>Meetings primarily deal with three things: proposing agendas, identifying problems, and reaching conclusions.</p>
<ul>
<li>Meetings should have more than just topics; agendas are even better.</li>
<li>Meetings shouldn't solve problems, just identify and track them.</li>
<li>Meetings must reach a consensus and conclusion. If not, treat it as a problem for the responsible individual to handle.</li>
</ul>
<p>For weekly or ad-hoc team meetings (excluding private discussions), the meeting organizer should gather topics in advance, categorized as:</p>
<ul>
<li><strong>Project-Based</strong>: A prior project progress plan is required.</li>
<li><strong>Solution-Based</strong>: Relevant designs must be prepared before discussions.</li>
<li><strong>Problem-Based</strong>: Problems and solutions must be documented.</li>
<li><strong>Decision-Based</strong>: The context, repercussions, and a pros-cons analysis should be available.</li>
<li><strong>Information-Based</strong>: Necessary details should be prepared.</li>
</ul>
<p>Meeting organizers should send out meeting topics by Friday.</p>
<p>&quot;Ensure that every meeting is efficient and productive.&quot;</p>
<h3 id="_9-1-2-3-escalation-1" tabindex="-1"><a class="header-anchor" href="#_9-1-2-3-escalation-1" aria-hidden="true">#</a> 9) 1-2-3 Escalation</h3>
<p>When facing problems, if you can't find a solution within 1 hour, discuss with others. If after 2 hours of discussion there's no resolution, elevate it to the whole team. If after 3 hours the team hasn't found a solution, it's time to seek external assistance.</p>
<p>&quot;When encountering problems, if you can't resolve them within 1 hour, discuss with colleagues. If it's unresolved after 2 hours, escalate it to the team. If after 3 hours the team can't find a solution, consider seeking external help.&quot;</p>
<h3 id="a-3ps-update-1" tabindex="-1"><a class="header-anchor" href="#a-3ps-update-1" aria-hidden="true">#</a> A) 3PS Update</h3>
<p>When updating progress, make it more meaningful than just a check-in. At the end of a task, summarize your work. Practice the <strong>3PS – Plan, Priority, Problem, Summary – What's your plan? What's the priority? What problems did you face? What's your current task summary?</strong></p>
<p>&quot;During progress updates, share your Plan, Priority, Problem, and a Summary of your work.&quot;</p>
<h3 id="b-disagree-and-commitment-1" tabindex="-1"><a class="header-anchor" href="#b-disagree-and-commitment-1" aria-hidden="true">#</a> B) Disagree and Commitment</h3>
<p>During development, team members will have different styles and opinions, leading to disagreements. We encourage disagreements, but only before a team decision is made. Once a decision is made, all must support and contribute towards it. However, the decision-making process can be filled with debates. Guidelines to handle disputes include:</p>
<ul>
<li>Owners must push forward significant discussions, aiming for swift conclusions.</li>
<li>During decision-making, minutes must be updated to Github's related project Issue or Pull Request</li>
</ul>

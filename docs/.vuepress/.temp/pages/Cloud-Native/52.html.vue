<h1 id="k8sgpt" tabindex="-1"><a class="header-anchor" href="#k8sgpt" aria-hidden="true">#</a> k8sgpt</h1>
<p>k8sgpt is a tool for scanning your Kubernetes clusters, diagnosing, and triaging issues in simple English. It has SRE experience codified into its analyzers and helps to pull out the most relevant information to enrich it with AI.</p>

<h2 id="project-overview" tabindex="-1"><a class="header-anchor" href="#project-overview" aria-hidden="true">#</a> Project Overview</h2>
<p>CloudBuildAI is an automated cloud-native application building and deployment tool that uses AI technology to generate Dockerfiles, Kubernetes YAML files, and other cloud platform deployment configurations for users.</p>
<p>In cloud-native application development, building Docker images and writing Kubernetes deployment files is a tedious and important task, and that's where CloudBuildAI comes in.</p>
<p>CloudBuildAI can automatically generate Dockerfiles and Kubernetes deployment files by inputting a GitHub repository address and automatically deploy the application on Sealos. If the files generated by AI are not accurate enough, CloudBuildAI can continuously optimize the accuracy of file generation through automatic repair and human feedback.</p>
<h2 id="project-purpose" tabindex="-1"><a class="header-anchor" href="#project-purpose" aria-hidden="true">#</a> Project Purpose</h2>
<ul>
<li>Simplify the cloud-native application deployment process</li>
<li>Make it easy for more developers to achieve automated CI/CD</li>
<li>Generate accurate Dockerfiles and Kubernetes YAML files through AI technology</li>
<li>Continuously optimize and improve, fix incorrect configurations, and improve deployment efficiency</li>
</ul>
<h2 id="quick-start" tabindex="-1"><a class="header-anchor" href="#quick-start" aria-hidden="true">#</a> Quick Start</h2>
<ol>
<li>Submit your GitHub repository address to CloudBuildAI</li>
<li>CloudBuildAI analyzes the code and generates Dockerfile and Kubernetes YAML files</li>
<li>Check the generated files and submit feedback to CloudBuildAI</li>
<li>CloudBuildAI continuously optimizes and improves</li>
<li>Merge the files generated by CloudBuildAI into the repository to achieve automated deployment!</li>
</ol>

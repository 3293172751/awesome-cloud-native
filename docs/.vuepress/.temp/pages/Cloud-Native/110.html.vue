<li><strong>openim-rpc-friend</strong>: Manage friend lists and related functionalities through RPC.</li>
<li><strong>openim-rpc-group</strong>: Group management via RPC.</li>
<li><strong>openim-rpc-msg</strong>: Message handling at the RPC level.</li>
<li><strong>openim-rpc-third</strong>: Third-party integrations and related tasks through RPC.</li>
<li><strong>openim-rpc-user</strong>: User management and tasks via RPC.</li>
</ul>

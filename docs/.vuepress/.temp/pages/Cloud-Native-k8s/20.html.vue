<h2 id="是什么" tabindex="-1"><a class="header-anchor" href="#是什么" aria-hidden="true">#</a> 是什么</h2>
<ul>
<li>
<p><code v-pre>StatefulSet</code> 是用来管理有状态的应用，例如数据库。</p>
</li>
<li>
<p>前面我们部署的应用，都是不需要存储数据，不需要记住状态的，可以随意扩充副本，每个副本都是一样的，可替代的。</p>
</li>
<li>
<p>而像数据库、<code v-pre>Redis</code> 这类有状态的，则不能随意扩充副本。</p>
</li>
<li>
<p><code v-pre>StatefulSet</code> 会固定每个 <code v-pre>Pod</code> 的名字</p>
</li>
</ul>
